import { NextRequest } from "next/server";
import { getServerSession } from "next-auth";
import { authOptions } from "@/app/api/auth/[...nextauth]/options";
import { prisma } from "@/lib/db";
import { generateUserInsight, getUserInsights } from "@/lib/insights";
import { safeAsync, successResponse, unauthorizedError, notFoundError } from "@/lib/api-handler";

export const POST = safeAsync(async (req: NextRequest) => {
  const session = await getServerSession(authOptions);

  if (!session?.user?.email) {
    return unauthorizedError('Unauthorized');
  }

  const user = await prisma.user.findUnique({
    where: { email: session.user.email },
  });

  if (!user) {
    return notFoundError('User');
  }

  const insight = await generateUserInsight(user.id);

  return successResponse({ insight }, 'Insight generated successfully');
});

